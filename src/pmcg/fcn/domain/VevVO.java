package pmcg.fcn.domain;

import gerador.annotation.ZKEntity;
import gerador.annotation.ZKField;
import gerador.annotation.ZKId;

@ZKEntity(label = "Vev", sequence = "seqvev")
public class VevVO {
	@ZKId
	@ZKField(label = "Código", artigo = "o", readonly = true, maxsize = 80, tooltip = "Código incremental!")
	private Long iddvev; //iddvev
	private Long viavev; //viabilidade
	private String evevev; //codigo evento
	
	public Long getIddvev() {
		return iddvev;
	}
	public void setIddvev(Long iddvev) {
		this.iddvev = iddvev;
	}
	public Long getViavev() {
		return viavev;
	}
	public void setViavev(Long viavev) {
		this.viavev = viavev;
	}
	public String getEvevev() {
		return evevev;
	}
	public void setEvevev(String evevev) {
		this.evevev = evevev;
	}
	
}
