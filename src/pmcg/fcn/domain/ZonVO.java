package pmcg.fcn.domain;

import java.util.Date;

import gerador.annotation.ZKEntity;
import gerador.annotation.ZKField;
import gerador.annotation.ZKId;

@ZKEntity(label = "Zona - Imóvel", sequence = "seqzon")
public class ZonVO {
	@ZKId
	@ZKField(label = "Código", artigo = "o", readonly = true, maxsize = 80, tooltip = "Código incremental!")
	private Long iddzon; //iddzon
	private String codzon; //codigo zona
	private String deszon; //descricao
	private String usualt; //usualt
	private Date datalt; //datalt
	
	public Long getIddzon() {
		return iddzon;
	}
	public void setIddzon(Long iddzon) {
		this.iddzon = iddzon;
	}
	public String getCodzon() {
		return codzon;
	}
	public void setCodzon(String codzon) {
		this.codzon = codzon;
	}
	public String getDeszon() {
		return deszon;
	}
	public void setDeszon(String deszon) {
		this.deszon = deszon;
	}
	public String getUsualt() {
		return usualt;
	}
	public void setUsualt(String usualt) {
		this.usualt = usualt;
	}
	public Date getDatalt() {
		return datalt;
	}
	public void setDatalt(Date datalt) {
		this.datalt = datalt;
	}
	
	

}
