package pmcg.imti.domain;

import gerador.annotation.ZKEntity;
import gerador.annotation.ZKField;
import gerador.annotation.ZKId;

import java.util.Date;

@ZKEntity(label = "Via", sequence = "seqvia")
public class ViaVO {
	@ZKId
	@ZKField(label = "Código", artigo = "o", readonly = true, maxsize = 80, tooltip = "Código incremental!")
	public Long con_id; //protocolo viabilidade
	public String sol_id; //solicitante
	public Integer tip_id;
	public Date con_datini; // Data de Início da Consulta
	public Date con_datfim; // Data de Término da Consulta
	public String con_status; //
	public Boolean con_impresso; //
	public String con_risco; // Tipo do Risco: B-Baixo / M-Médio / A-Alto
	public String con_licenciado; // Processo Licenciado
	public String con_autenticidade; //
	  	  
	public Long getCon_id() {
		return con_id;
	}
	public void setCon_id(Long con_id) {
		this.con_id = con_id;
	}
	public String getSol_id() {
		return sol_id;
	}
	public void setSol_id(String sol_id) {
		this.sol_id = sol_id;
	}
	public Integer getTip_id() {
		return tip_id;
	}
	public void setTip_id(Integer tip_id) {
		this.tip_id = tip_id;
	}
	public Date getCon_datini() {
		return con_datini;
	}
	public void setCon_datini(Date con_datini) {
		this.con_datini = con_datini;
	}
	public Date getCon_datfim() {
		return con_datfim;
	}
	public void setCon_datfim(Date con_datfim) {
		this.con_datfim = con_datfim;
	}
	public String getCon_status() {
		return con_status;
	}
	public void setCon_status(String con_status) {
		this.con_status = con_status;
	}
	public Boolean getCon_impresso() {
		return con_impresso;
	}
	public void setCon_impresso(Boolean con_impresso) {
		this.con_impresso = con_impresso;
	}
	public String getCon_risco() {
		return con_risco;
	}
	public void setCon_risco(String con_risco) {
		this.con_risco = con_risco;
	}
	public String getCon_licenciado() {
		return con_licenciado;
	}
	public void setCon_licenciado(String con_licenciado) {
		this.con_licenciado = con_licenciado;
	}
	public String getCon_autenticidade() {
		return con_autenticidade;
	}
	public void setCon_autenticidade(String con_autenticidade) {
		this.con_autenticidade = con_autenticidade;
	}
	  
	  

}
	
